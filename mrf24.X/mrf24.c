/************ CONFIGURACIONES ****************/

void Port_Config(void){
    TRISD=0;
    LATD=0;
}

void SPI_Config(void){
    SSPSTAT=0x00;
    SSPCON1=0;
    SSPCON1bits.SSPEN=1;
    TRISCbits.RC3=0;
    TRISCbits.RC4=1;
    TRISCbits.RC5=0;
    
    INTCON=0xc0;
    PIE1bits.SSPIE=1;
}

